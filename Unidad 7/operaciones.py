def suma (num_a, num_b):
    return f"{num_a} + {num_b} = {(num_a + num_b)}"

def resta (num_a, num_b):
    return f"{num_a} - {num_b} = {(num_a - num_b)}"

def multiplicacion (num_a, num_b):
    return f"{num_a} * {num_b} = {(num_a * num_b)}"

def division (num_a, num_b):
    return f"{num_a} / {num_b} = {(num_a / num_b)}"